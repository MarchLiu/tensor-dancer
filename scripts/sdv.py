import numpy as np

data = [
    [76.885,        218.911,        182.344,        16.8575,        13.6391,        ],
    [173.381,       166.54, 113.949,        90.2699,        144.628,        ],
    [242.898,       181.733,        89.8876,        111.806,        199.278,        ],
    [89.2232,       82.7629,        243.187,        112.95, 55.1066,        ],
    [81.9365,       165.8,  160.826,        160.509,        40.161, ],
]

A = np.array(data)

# 计算奇异值分解
U, S, VT = np.linalg.svd(A, full_matrices=False)

# 打印结果
print("左奇异矩阵 U:")
print(U)

print("\n奇异值 S (按从大到小排序):")
print(S)

print("\n右奇异矩阵 VT:")
print(VT)
